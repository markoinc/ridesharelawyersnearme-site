export function getCityBySlug(slug: string): City | undefined {
  return cities.find(c => c.slug === slug);
}

export function getCitiesByState(stateSlug: string): City[] {
  return cities.filter(c => c.stateSlug === stateSlug);
}

export function getTopMarkets(limit: number = 20): City[] {
  return cities
    .filter(c => c.uberMarketRank !== undefined)
    .sort((a, b) => (a.uberMarketRank || 999) - (b.uberMarketRank || 999))
    .slice(0, limit);
}

export function getCitiesWithShortSOL(): City[] {
  const shortSOLStates = ['louisiana', 'tennessee'];
  return cities.filter(c => shortSOLStates.includes(c.stateSlug));
}

export function getContributoryNegligenceCities(): City[] {
  const cnStates = ['alabama', 'maryland', 'north-carolina', 'virginia', 'washington-dc'];
  return cities.filter(c => cnStates.includes(c.stateSlug));
}
